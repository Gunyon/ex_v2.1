	<p> În perioada 22-25 iunie 2010 la Chişinău s-a desfăşurat săptămâna investiţională "Moldova Business Week 2010". În cadrul  evenimentului, Guvernatorul Băncii Naţionale a Moldovei Dorin Drăguţanu a prezentat audienţei situaţia privind indicatorii economici şi financiari ai Republicii Moldova.</p>
